import { SourceKey } from "./sources";

export interface FightRecord {
  date: string;
  opponent: string;
  result: "win" | "loss" | "draw" | "nc";
  method: string;
  event: string;
  round: string;
  // --- 加算的拡張(2026-07 DEEP選手取得)。既存データは未設定=省略のままで動く ---
  // 決着時間(分:秒。例 "3:22")。roundフィールドから分離して保持する場合に使う。
  time?: string;
  // その試合ごとの階級/契約体重(例 "DEEPフェザー級", "68kg契約")。
  weightClass?: string;
}

export interface Fighter {
  slug: string;
  nameJa: string;
  nameEn: string;
  org: SourceKey;
  weightClass: string;
  wins: number;
  losses: number;
  draws: number;
  ko: number;
  sub: number;
  decision: number;
  history: FightRecord[];
  // English Wikipedia article title with a "{{MMA record start}}" table.
  // Only set when one is known to exist; omitted fighters keep seed data.
  wikiTitleEn?: string;
  // jp.ufc.com/athlete/<slug> — UFC所属選手のみ。ニックネームをWikipediaより
  // 優先してUFC公式プロフィールから取得するために使う。
  ufcSlug?: string;
  // 日本語版WikipediaのFight-cont戦績データを使う場合のページ名。
  wikiTitleJa?: string;
  // 通称（ニックネーム）を表示しない選手向けフラグ。
  noNickname?: boolean;
  // 通称を固定値で指定する場合に使う。設定時はUFC公式/Wikipediaからの
  // 自動取得より優先される（noNicknameより後で判定）。
  nickname?: string;
  // 新規投入選手(DEEP等)を「表に出さない」ためのフラグ。true の場合:
  // サイトマップ非掲載 / 選手ページを noindex / 内部リンク非生成 にする。
  // Mレーティング(序列)や自動文脈が乗るまで、戦績テーブルだけの薄いページを
  // 一斉公開しない(SEO保護)ための制御。データ自体は格納・保持する。
  hidden?: boolean;
  // 戦績(history)と現戦績(wins/losses…)を自社 EVENT_RESULTS から動的に
  // 組み立てる選手。Wikipedia記事が無いDEEP選手のスタブに付ける。
  // resolveFighter が EVENT_RESULTS を選手軸に組み替えて注入する。
  recordFromResults?: boolean;
  // ローマ字表記が単一ソース由来などで最終確認が必要な選手。人間の裏取り待ち。
  // 表示・戦績には影響しない(hidden 前提のメタ情報)。
  needsReview?: boolean;
}

