export function getFighter(slug: string): Fighter | undefined {
  return FIGHTERS.find((f) => f.slug === slug);
}

export interface FighterRates {
  winRate: number | null; // 勝率（%） wins / (wins+losses)。引き分けは母数に含めない
  finishRate: number | null; // フィニッシュ率（%） (KO+一本) / wins
}

export function calcFighterRates(f: Pick<Fighter, "wins" | "losses" | "draws" | "ko" | "sub">): FighterRates {
  // 勝率は勝敗のみで算出（引き分けは母数から除外）
  const decided = f.wins + f.losses;
  const winRate = decided > 0 ? Math.round((f.wins / decided) * 100) : null;
  const finishRate = f.wins > 0 ? Math.round(((f.ko + f.sub) / f.wins) * 100) : null;
  return { winRate, finishRate };
}

// 任意の文字列（対戦カード上の選手名など）からMニュース掲載選手を探す。
// 大会結果ページ・選手の試合履歴テーブル双方の内部リンク生成で使う共通ロジック。
export function findFighterSlugByName(name: string, excludeSlug?: string): string | null {
  // スペース・全角スペースを除いて正規化して照合する
  const norm = (s: string) => s.replace(/[\s　]/g, "");
  const normName = norm(name);
  const match = FIGHTERS.find((f) => {
    if (f.slug === excludeSlug) return false;
    // hidden 選手には内部リンクを張らない(Mレーティングが乗るまで動線に出さない)
    if (f.hidden) return false;
    if (norm(f.nameJa) === normName) return true;
    if (f.nameEn === name) return true;
    // 英名を正規化して一致確認（大文字小文字を無視）
    if (f.nameEn.toLowerCase() === name.toLowerCase()) return true;
    return false;
  });
  return match ? match.slug : null;
}
